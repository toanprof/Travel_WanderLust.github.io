                                <br />
                                <p>Over the years, phở gained traction in Hanoi. It evolved from a noodle soup called
                                    xáo trâu -- a simple dish made with slices of water buffalo meat cooked in broth
                                    with rice vermicelli -- into a delicate and balanced creation. Buffalo meat was
                                    swapped with beef, round rice noodles were added, the flavour of the broth was
                                    refined, and the classic Hanoi phở was perfected.</p>
                                <p>Migrant workers from the Chinese provinces of Yunnan and Guangdong loved the new
