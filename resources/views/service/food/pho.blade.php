                                <p>While debate rages as to the location of Vietnam’s best pho, it could be argued that
                                    the dish hasn’t actually evolved all that radically since the early years. Different
                                    cuts of meat have been brought to the table and diners can choose from a range of
                                    beef cuts including rare beef (tái), flank (nạm), brisket (gầu), tripe (sách),
                                    tendon (gan) and meatballs (bò viên). The invention of chicken phở (phở gà) in 1939
                                    caused ructions for a while. But, by and large, pho has stayed true to its original
                                    tenets. </p>
                                <br />
                                <p>That said, phở is not an immovable feast. Some young chefs in Vietnam are
                                    experimenting with items such as brown rice noodles and fresh pho noodle rolls. In
                                    2018, Anan Saigon famously introduced a 100$ phở with truffle oil, wagyu and foie
                                    gras. And overseas, chefs are getting even more inventive with additions such as
                                    crawfish and sous vide beef.</p>
                                <p>These days, beef bones, flank steak oxtails, charred onion, charred ginger and spices
                                    including star anise, cinnamon, cloves, black cardamom and coriander are used to
                                    make the slow-cooked broth. Chicken phở is an equally popular alternative to the
                                    original. In the north, garnishes are limited to fresh chilli slices, lemon, and a
                                    few herbs; but in the south, the phở is noticeably sweeter and there's a wide array
                                    of types to choose from. In Central Vietnam, you can even find phở with poached
                                    eggs. </p>
