                                <p>The history of phở begins at the end of the 19th century, at the peak of French
                                    colonialism. French demand led to a greater availability of beef in Vietnam. This in
                                    turn produced a surplus of beef bones, which were used by Chinese and Vietnamese
                                    vendors to deepen and perfect the flavour of the Nam Dinh broth.
