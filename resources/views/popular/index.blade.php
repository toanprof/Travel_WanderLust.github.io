                        <h3 class="roboto-slab">2. Hoi An </h3>
                        <p>Hội An is a place where the bold march of progress peters into a leisurely amble. In this
                            one-time trading port, the Thu Bồn River meanders past crumbling shop houses and weathered
                            pagodas, while sampans come and go from the old ferry quay. Outside the Old Town, two-lane
                            roads slice through waving rice fields and emerge at a frothy coastline. Stay a couple of
                            days, and Hội An's easygoing beauty and lantern-lit nights may leave you hopelessly
                            beguiled.</p>
                        <br />
                        <p>The international airport in Đà Nẵng is a 50-minute drive from the Ancient Town. There are
                            domestic flights from Vietnam’s major cities to Đà Nẵng, as well as direct flights from
                            regional hubs. Hội An can also be reached by bus or shuttle vans from nearby destinations,
                            such as Nha Trang, Đà Nẵng or Huế. Walking and cycling is ideal for sightseeing in the
                            Ancient Town and the countryside. Taxis and ride-hailing apps can also take you around. Ask
                            your hotel about a private car for trips further afield.</p>
