                        src="https://cdn3.ivivu.com/2013/09/gieng-troi-diem-hap-dan-cho-dan-phuot-da-thanh-ivivu-12.jpg"
                        alt="">
                    <p style="text-align: justify;">Xuất phát từ bãi giữ xe của khu cáp treo Bà Nà, phượt thủ phải chinh
                        phục 7,5 km đường
                        đèo dốc quanh co, vượt suối băng rừng để đến được “phượt điểm” này. Qua được con dốc đầu tiên
                        thì chặng đường còn
                        lại rất dễ đi.&nbsp;Cũng vì thế mà nơi đây như một chốn biệt lập và hoang sơ.</p>
                    <p style="text-align: justify;"><strong>Rạn Nam Ô:&nbsp;</strong>Cách trung tâm làng Nam Ô (phường
                        Hòa Hiệp Nam, quận
                        Liên Chiểu, Đà Nẵng – nằm giữa Đà Nẵng và đèo Hải Vân) khoảng 2km về hướng Đông Nam có một dải
                        đá ngầm, ngư dân
                        trong vùng gọi là rạn Nam Ô. Rạn Nam Ô có nhiều rong tảo nên là nơi trú ngụ của nhiều loài cá và
                        hải sản quý.</p>
                    <img class="wp-image size-full" src="https://cdn3.ivivu.com/2013/09/du-lich-da-nang-ivivu-7.jpg"
                        alt="">
                    <h2 style="text-align: justify;">Ăn: Quán ăn, ăn vặt, đặc sản của du lịch Đà Nẵng</h2>
                    <p style="text-align: justify;">Đà Nẵng có hai chợ lớn nhất nằm ở trung tâm thành phố là chợ Hàn và
                        chợ Cồn; cùng những
                        siêu thị lớn mới mở trong vòng vài năm trở lại đây như siêu thị Đà Nẵng, siêu thị Metro, đại
                        siêu thị BigC, siêu thị
                        Intimex, siêu thị Rosa Bài Thơ, siêu thị Nhật Linh, siêu thị Đại Dương… Đây là những trung tâm
                        thương mại chủ yếu
                        của Đà Nẵng.</p>
                    <p><strong>Các quán ăn ngon ở Đà
                            Nẵng</a></strong> phục vụ nhiều món ăn đặc sản như mì Quảng, bánh tráng cuốn thịt heo, Bánh
                        xèo, thịt bê
                        thui, bún chả cá, bún mắm, bánh khô mè, nước mắm Nam Ô.. có thể mua về làm quà biếu.</p>
                    <p><strong> 12 MÓN NGON ĐÀ NẴNG</strong></p>
                    <p><strong> 1. Mì Quảng</strong></p>
                    <p>Mì Quảng bán buổi sáng tới tầm 9h – 10h ở đường Hoàng Diệu, Phan Thanh, Trưng Nữ Vương, Hoàng Hoa
                        Thám; một số nơi
                        bán cả ngày như trên đường Nguyễn Hữu Thọ, Nguyễn Công Trứ. Giá cả dao động tùy nơi, tùy tô lớn
                        nhỏ khoảng 13.000 –
                        25.000 đồng/tô, bánh tráng bán kèm 3.000 đồng/cái. Mì Quảng số 1A Hải Phòng; Mì quảng bà Vị: 155
                        Trưng Nữ Vương; Giá
                        tham khảo: 20.000 – 30.000 VND/tô.</p>
                    <img class="wp-image size-full" src="https://cdn3.ivivu.com/2013/09/am-thuc-da-nang-ivivu4.jpg"
                        alt="">
                    <p><strong>2. Gỏi cá Nam Ô</strong></p>
                    <p>Gỏi cá Nam Ô thì phải tới khu vực Nam Ô để thưởng thức mới đúng điệu. Hoặc nếu không, bạn có thể
                        qua Nguyễn Tất Thành
                        hay cầu Nam Ô và gọi một phần với giá từ 40.000 đồng.</p>
                    <img class="wp-image size-full"
                        src="https://cdn3.ivivu.com/2013/09/ve-da-nang-an-goi-ca-nam-o-ivivu-2.jpg" alt="">
                    <p><strong>3. Bún chả cá</strong></p>
                    <p style="text-align: justify;">Quán trên đường Hoàng Diệu (ngay bên cạnh tòa nhà Hoàng Anh Gia Lai)
                        bán cả ngày từ 7h –
                        21h; quán bán suốt đêm trên đường Hùng Vương; quán lại chỉ bán buổi sáng từ 6h – 10h ở đường
                        Trần Cao Vân (đối diện
                        chợ Tam Tòa). Giá từ 15.000 đồng/tô. Bún chả cá gia truyền 109 Nguyễn Chí Thanh – quận Hải Châu.
                        Giá: 20 nghìn/tô
                        bình thường và 25 nghìn/tô đặc biệt; thời gian phục vụ từ 6h – 22h, giờ cao điểm là 7h – 9h sáng
                        và 17h – 20h tối.
                    </p>
                    <p><strong>4. Bánh tráng thịt heo</strong></p>
                    <p style="text-align: justify;">Để tròn vị, bạn nên chọn quán trên đường Châu Thị Vĩnh Tế hoặc Hải
                        Phòng, Duy Tân, Lê
                        Duẩn, Đỗ Thúc Thịnh… với giá khoảng từ 30.000 – 80.000 đồng. Bánh tráng cuốn thịt heo quán Mậu,
                        35 Đỗ Thúc Tịnh,
                        bánh tráng Trần số 4 Lê Duẩn.</p>
                    <img class="wp-image size-full"
                        src="https://cdn3.ivivu.com/2013/09/banh-trang-cuon-thit-heo-ivivu.jpg" alt="">
                    <p><strong>5. Bánh xèo</strong></p>
                    <p style="text-align: justify;">Giá bành xèo từ 5.000 đồng/cái trên đường Hoàng Diệu, Hải Phòng…
                        Bánh xèo bà Dưỡng trong
                        kiệt 11 (ngõ/hẻm 11) phố Hoàng Diệu, địa chỉ mới: K280/23 Hoàng Diệu. Chú ý: có một số quán khác
                        cũng mở ra ở trước
                        đó nên bạn chú ý biển tên để vào cho đúng nhé.</p>
                    <p style="text-align: justify;"><strong>6. Bánh bèo</strong></p>
                    <p style="text-align: justify;">Không kể đến những gành hàng rong thì khu bánh bèo ở chợ Cồn (cổng
                        đường Hùng Vương),
                        Hoàng Diệu, Hải Phòng, Ông Ích Khiêm (bán từ trưa, chỉ từ tháng 9 – tháng 2 hàng năm)… là chỗ
                        nên thử. Mỗi chén bánh
                        bèo chỉ khoảng 1.500 đồng.</p>
                    <img class="wp-image size-full" src="https://cdn3.ivivu.com/2013/09/banh-beo-da-nang-ivivu-1.jpg"
                        alt="">
                    <p style="text-align: justify;"><strong>7. Bê thui Cầu Mống</strong></p>
                    <p style="text-align: justify;">Bê thui Cầu Mống ngon nhất là ở Cầu Mống, huyện Điện Bàn, tỉnh Quảng
                        Nam, cách Đà Nẵng
                        15km. Ngoài ra còn có Bê thui Cầu Mống bà Ngọc 228 Đống Đa, 99 Ông Ích Khiêm.</p>
                    <p><strong>8. Chè xoa xoa hạt lựu</strong></p>
                    <p style="text-align: justify;">Có nhiều quán xoa xoa hạt lựu nhưng ngon nhất vẫn là ở chợ Cồn, hay
                        một số quán trên
                        đường Trần Bình Trọng (ngay ngã ba Trần Bình Trọng và Ngô Gia Tự), Phan Thanh… với giá chỉ từ
                        5.000 đồng/ly.</p>
                    <img class="wp-image size-full" src="https://cdn3.ivivu.com/2013/09/che-xoa-coa-hat-luu-ivivu.jpg"
                        alt="">
                    <p style="text-align: justify;"><strong>9. Mít trộn</strong></p>
                    <p style="text-align: justify;">Ngon nhất là trên đường Ông Ích Đường (đối diện dệt may Hòa Thọ),
                        Phạm Văn Nghị… Giá rất
                        bình dân, chỉ khoảng 10.000 đồng/ đĩa mít.</p>
                    <p style="text-align: justify;"><strong>10. Bánh tráng kẹp</strong></p>
                    <p style="text-align: justify;">Báng tráng có giá bán từ 8.000đ/1 dĩa trở lên. Ngon nhất là ở ngõ
                        gần khách sạn Thăng
                        Long, đường Điện Biên Phủ và quán Bánh tráng tương, báng tráng đập Bà Tứ: 354 Lê Duẩn; giá 3.000
                        – 5.000 VND/cái.
                    </p>
                    <img class="wp-image size-full" src="https://cdn3.ivivu.com/2013/09/banh-trang-kep-ivivu.jpg"
                        alt="">
                    <p style="text-align: justify;"><strong>11. Kem xôi</strong></p>
                    <p>Giá khoảng từ 7.000đ- 12.000đ/1 ly ở đường Ngô Sĩ Liên, Hòa Khánh.</p>
                    <p><strong>12. Ốc hút</strong></p>
                    <img class="wp-image size-full"
                        src="https://cdn3.ivivu.com/2013/09/45-dieu-khien-ban-yeu-dien-cuong-du-lich-da-nang-iVIVU.com-24.jpg"
                        alt="">
                    <p>Giá một đĩa ốc hút trung bình từ 20.000đ trở lên, với các loại ốc ngon như ốc đá, ốc bưu… thì giá
                        sẽ nhỉnh hơn một
                        chút. Ở Đà Nẵng có rất nhiều quán ốc nhưng được nhiều người khen nhất phải kể đến quán ốc trên
                        đường Lê Duẩn.<br></p>
                    <h2>Địa điểm chụp ảnh đẹp ở Đà Nẵng</h2>
                    <p><strong>Khu du lịch Bà Nà Hills</strong></p>
                    <p style="text-align: justify;">Được mệnh danh là chốn tiên cảnh với khí hậu 4 mùa vô cùng đặc biệt,
                        Bà Nà Hills là nơi
                        được nhiều các bạn trẻ chụp hình và check-in khi đến Đà Nẵng. Không gian thiên nhiên rộng rãi
                        với một màu xanh tươi
                        mát cộng với cảnh quan tuyệt đẹp ở Bà Nà, chắc hẳn sẽ làm phông nền lý tưởng cho những bức ảnh
                        của bạn.</p>
                    <p style="text-align: justify;"><strong>Khu vực tượng đá Cá Chép Hóa Rồng và cầu tàu tình
                            yêu</strong></p>
                    <p style="text-align: justify;">Đây là hình ảnh được chọn làm biểu tượng cho bến du thuyền, tạo điểm
                        nhấn mới cho cảnh
                        quan bờ Đông sông Hàn . Thân cá chép được điêu khắc với lớp vẩy rắn chắc. Đầu hóa rồng lấy ý
                        tưởng rồng thời Lý và
                        được thiết kế phun mưa. Đuôi cá được làm cách điệu, mang ý nghĩa hòa bình, thịnh vượng, tình
                        đoàn kết…</p>
                    <img class="wp-image size-full" src="https://cdn3.ivivu.com/2013/09/du-lich-da-nang-ivivu-9.jpg"
                        alt="">
                    <p style="text-align: justify;">Tại đây, còn có cây cầu tình yêu vốn được giới trẻ biết đến như một
                        nơi để bày tỏ tình
                        yêu, sự hứa hẹn hạnh phúc. Cùng với đó là không gian đẹp bên chân cầu Rồng, đây sẽ là nơi chụp
                        ảnh kỷ niệm lý tưởng
                        được nhiều bạn trẻ chọn lựa.</p>
